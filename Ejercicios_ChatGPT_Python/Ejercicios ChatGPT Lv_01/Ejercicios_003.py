"""area de un triángulo"""
# Pide base y altura, y calcula el área

print("Introduce la base del triángulo: ")
base = float(input())
print("Introduce la altura del triángulo: ")
altura = float(input())
area = (base * altura) / 2
print(f"El área del triángulo con base {base} y altura {altura} es: {area}")